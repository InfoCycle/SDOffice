package com.info.web;

/*
 * 页面请求的参数,2012-12-4 jibinbin
 * 用于：JQueryDataTables
 */
public class JSONParam {
	private String name;
	private String value;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
